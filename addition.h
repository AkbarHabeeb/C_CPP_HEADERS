int add(int a[],int n)
{
	int sum=0;
	for(int i=0;i<n;i++)
	sum=sum+a[i];
	return sum;
}


float add(float a[],int n)
{
	float sum=0.0;
	for(int i=0;i<n;i++)
	sum=sum+a[i];
	return sum;
}


double add(double a[],int n)
{
	double sum=0;
	for(int i=0;i<n;i++)
	sum=sum+a[i];
	return sum;
}
